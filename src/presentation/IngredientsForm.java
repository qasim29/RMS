    private boolean checkValidation() {
        if (ingredientName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(IngredientsForm.this, "Enter Name");
            ingredientName.grabFocus();
            return false;
        } else if (ingredientQty.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(IngredientsForm.this, "Enter Quantity");
            ingredientName.grabFocus();
            return false;
        }
        return true;
    }
    
    private void addRow(int id, String name, float qty, String decs) {
        model.insertRow(ingredientTbl.getRowCount(), new Object[]{id, name, qty, decs});
    }

