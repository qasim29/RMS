    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if (checkValidation()) {
            String ingName = ingredientName.getText();
            int ingQty = Integer.parseInt(ingredientQty.getText());
            String desc = ingDesc.getText();
            boolean flag = Ingredient_DAO.add(new Ingredients(ingName, ingQty, desc));
            if (flag) {
                JOptionPane.showMessageDialog(IngredientsForm.this, "...Ingredient is added...");
                load();
            } else {
                JOptionPane.showMessageDialog(IngredientsForm.this, "something went wrong...");
            }
            reSet();
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int i = ingredientTbl.getSelectedRow();
        if (i == -1) {
            JOptionPane.showMessageDialog(IngredientsForm.this, "Select any Row");
        } else {
            int id = Integer.parseInt(model.getValueAt(i, 0).toString());
            Ingredient_DAO.deleteRow(id);
            load();
            reSet();
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void ingredientTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ingredientTblMouseClicked
        // TODO add your handling code here:
        reSet();
        int i = ingredientTbl.getSelectedRow();
        if (i != -1) {
            ingredientId.setText(model.getValueAt(i, 0).toString());
            ingredientName.setText(model.getValueAt(i, 1).toString());
            ingredientQty.setText(model.getValueAt(i, 2).toString());
            ingDesc.setText(model.getValueAt(i, 3).toString());
        } else {
            JOptionPane.showMessageDialog(IngredientsForm.this, "Select any Row");
        }
    }//GEN-LAST:event_ingredientTblMouseClicked

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        int i = ingredientTbl.getSelectedRow();
        if (i != -1 && checkValidation() && !(ingredientId.getText().trim().isEmpty())) {
            int id = Integer.parseInt(ingredientId.getText());
            String name = ingredientName.getText();
            int qty = (int)(Float.parseFloat(ingredientQty.getText()));
            String desc = ingDesc.getText();
            Ingredient_DAO.update(id, name, qty, desc);
            load();
            reSet();
        } else {
            JOptionPane.showMessageDialog(IngredientsForm.this, "Select Row to update");
        }
    }//GEN-LAST:event_updateBtnActionPerformed

